package vehiculos;

public class Pais {
	//atributo clase
	private String nombre;
	
	public Pais(){
		
	}
	public Pais(String nombre) {
		this.nombre = nombre;
	}
	//set y get
	
	public String getNombre() {
		return this.nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
}
